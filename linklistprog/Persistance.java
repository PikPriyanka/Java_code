package linklistprog;

import java.util.*;

public class Persistance {
    public static void main(String Arg[])    {
       ArrayList <String> a = new ArrayList<String>();
          Scanner sc = new Scanner(System.in);
          int n = sc.nextInt();
          int count=1;
          for(int i =1;i<=n;i++){
             for(int j=1;j<=n;j++){
                 System.out.print(a.add("count*"));
                 count++;
          }
             System.out.print("\n");
      }
    }
}
